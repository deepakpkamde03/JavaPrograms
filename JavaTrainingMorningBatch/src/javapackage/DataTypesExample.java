package javapackage;

public class DataTypesExample {

	public static void main(String[] args) {

		// Below datatypes are used to store numeric values
		byte b = 1;
		short s = 2;
		int a = 100;
		long l = 1000;

		// Below datatypes are used to store decimal values
		float num = 99.99f;
		double num2 = 1000.33;

		// Below datatypes are used to store boolean value
		boolean bn = true;

		// Below datatypes are used to store char values
		char ch = 'A';
		
		System.out.println(b);// 1
		System.out.println(s);//2
		System.out.println(a);//100
		System.out.println(l);//1000
		System.out.println(num);//99.99
		System.out.println(num2);//1000.33
		System.out.println(bn);//true
		System.out.println(ch);//A
		
		

	}

}
