package operatorprograms;

public class AssignmentOperator {

	// Assignment Operator Program
	public static void main(String[] args) {

		/*
		 * Note: 
		 * Do not give space between + and = | - and =
		 * | * and = | / and =
		 */
		int a = 10;
		a += 3; // 13
		System.out.println(a);
		
		
		a -=4; // 13-4=9
		System.out.println(a);
		
		a *=2; // 18
		System.out.println(a);
		
		a /=2; // 9
		System.out.println(a);
		
		
		
	}

}
